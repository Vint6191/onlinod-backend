router.post("/start", async (req, res) => {
  try {
    const input = startSchema.parse(req.body);
    const token = randomToken(32);

    const result = await prisma.$transaction(async (tx) => {
      const creator = await tx.creatorAccount.create({
        data: {
          agencyId: req.auth.agencyId,
          displayName: input.displayName,
          username: input.username || null,
          status: "DRAFT",
          partition: input.partition || null,
        },
      });

      const partition = input.partition || makePartition(creator.id);

      const updatedCreator = await tx.creatorAccount.update({
        where: { id: creator.id },
        data: { partition },
      });

      const session = await tx.creatorConnectSession.create({
        data: {
          agencyId: req.auth.agencyId,
          creatorId: creator.id,
          createdByUserId: req.auth.userId,
          tokenHash: sha256(token),
          status: "PENDING",
          expiresAt: addMinutes(10),
        },
      });

      return {
        creator: updatedCreator,
        session,
      };
    });

    const connectUrl = `onlinod://connect?token=${encodeURIComponent(token)}`;

    await prisma.creatorConnectSession.update({
      where: { id: result.session.id },
      data: { connectUrl },
    });

    await audit({
      agencyId: req.auth.agencyId,
      actorUserId: req.auth.userId,
      action: "creator_connect.start",
      targetType: "creator",
      targetId: result.creator.id,
      metadata: {
        sessionId: result.session.id,
        username: result.creator.username,
      },
    });

    return res.status(201).json({
      ok: true,
      creator: result.creator,
      session: {
        id: result.session.id,
        status: result.session.status,
        expiresAt: result.session.expiresAt,
      },
      connectUrl,
      token,
      browserFallbackUrl: `${appBaseUrl(req)}/?connectSession=${encodeURIComponent(result.session.id)}`,
    });
  } catch (err) {
    if (err?.issues) return validationError(res, err);
    console.error("[creator-connect/start] failed:", err);
    return res.status(500).json({
      ok: false,
      code: "CONNECT_START_FAILED",
      error: "Failed to start creator connection",
    });
  }
});

router.post("/claim", async (req, res) => {
  try {
    const input = claimSchema.parse(req.body);
    const tokenHash = sha256(input.token);

    const session = await prisma.creatorConnectSession.findUnique({
      where: { tokenHash },
      include: { creator: true },
    });

    if (!session) {
      return res.status(404).json({ ok: false, code: "SESSION_NOT_FOUND", error: "Connect session not found" });
    }

    if (session.agencyId !== req.auth.agencyId) {
      return res.status(403).json({ ok: false, code: "FORBIDDEN", error: "Connect session belongs to another agency" });
    }

    if (session.status !== "PENDING" && session.status !== "CLAIMED") {
      return res.status(409).json({ ok: false, code: "SESSION_NOT_PENDING", error: `Session is ${session.status}` });
    }

    if (session.expiresAt < new Date()) {
      await prisma.creatorConnectSession.update({
        where: { id: session.id },
        data: { status: "EXPIRED" },
      });

      return res.status(410).json({ ok: false, code: "SESSION_EXPIRED", error: "Connect session expired" });
    }

    let device = null;
    if (input.deviceId || input.deviceName || input.platform || input.appVersion) {
      device = await prisma.workerDevice.upsert({
        where: { id: input.deviceId || `device_${req.auth.userId}_${Date.now()}` },
        create: {
          id: input.deviceId || undefined,
          agencyId: req.auth.agencyId,
          userId: req.auth.userId,
          deviceName: input.deviceName || null,
          platform: input.platform || null,
          appVersion: input.appVersion || null,
          lastSeenAt: new Date(),
        },
        update: {
          deviceName: input.deviceName || undefined,
          platform: input.platform || undefined,
          appVersion: input.appVersion || undefined,
          lastSeenAt: new Date(),
        },
      });
    }

    const updated = await prisma.creatorConnectSession.update({
      where: { id: session.id },
      data: {
        status: "CLAIMED",
        claimedByUserId: req.auth.userId,
        claimedByDeviceId: device?.id || input.deviceId || null,
        claimedAt: session.claimedAt || new Date(),
      },
    });

    await audit({
      agencyId: req.auth.agencyId,
      actorUserId: req.auth.userId,
      action: "creator_connect.claim",
      targetType: "creator_connect_session",
      targetId: session.id,
      metadata: { creatorId: session.creatorId, deviceId: device?.id || input.deviceId || null },
    });

    return res.json({
      ok: true,
      session: {
        id: updated.id,
        status: updated.status,
        expiresAt: updated.expiresAt,
      },
      creator: session.creator,
      partition: session.creator.partition || makePartition(session.creator.id),
      loginUrl: "https://onlyfans.com",
    });
  } catch (err) {
    if (err?.issues) return validationError(res, err);
    console.error("[creator-connect/claim] failed:", err);
    return res.status(500).json({
      ok: false,
      code: "CONNECT_CLAIM_FAILED",
      error: "Failed to claim creator connection",
    });
  }
});

