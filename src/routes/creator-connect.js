const express = require("express");
const { z } = require("zod");

const prisma = require("../prisma");
const { authRequired } = require("../middleware/auth");
const { randomToken, sha256, addMinutes } = require("../utils/crypto");
const { encryptSnapshot, hashUserAgent } = require("../services/snapshot-crypto");
const { audit } = require("../services/audit-service");

const router = express.Router();

const startSchema = z.object({
  displayName: z.string().min(1).max(120),
  username: z.string().max(120).optional().nullable(),
  partition: z.string().max(220).optional().nullable(),
});

const claimSchema = z.object({
  token: z.string().min(20),
  deviceId: z.string().max(160).optional().nullable(),
  deviceName: z.string().max(160).optional().nullable(),
  platform: z.string().max(120).optional().nullable(),
  appVersion: z.string().max(80).optional().nullable(),
});

const completeSchema = z.object({
  token: z.string().min(20).optional(),
  deviceId: z.string().max(160).optional().nullable(),
  snapshot: z.object({
    cookies: z.array(z.any()).optional().default([]),
    userAgent: z.string().max(2000).optional().nullable(),
    storage: z.any().optional().nullable(),
    remoteId: z.string().max(120).optional().nullable(),
    username: z.string().max(120).optional().nullable(),
    displayName: z.string().max(120).optional().nullable(),
    avatarUrl: z.string().max(2000).optional().nullable(),
    capturedAt: z.string().optional().nullable(),
  }),
});

function validationError(res, err) {
  return res.status(400).json({
    ok: false,
    code: "VALIDATION_ERROR",
    error: err.issues?.[0]?.message || "Validation error",
    issues: err.issues || [],
  });
}

function appBaseUrl(req) {
  return (process.env.APP_URL || process.env.PUBLIC_BASE_URL || `${req.protocol}://${req.get("host")}`).replace(/\/+$/, "");
}

function makePartition(creatorId) {
  return `persist:creator_${creatorId}`;
}


// ---------------------------------------------------------------------------
// Public token endpoints for Electron deep-link flow.
//
// These endpoints DO NOT require Onlinod login inside Electron.
// Authorization is the one-time connect token generated by the authenticated
// web console in POST /api/creator-connect/start.
//
// Flow:
//   Web authenticated owner → /start → onlinod://connect?token=...
//   Electron receives token → /claim-public
//   Electron logs into OF → /complete-public
//
// The token is still hashed in DB and expires quickly.
// ---------------------------------------------------------------------------

async function findActiveSessionByToken(token) {
  const tokenHash = sha256(token);

  const session = await prisma.creatorConnectSession.findUnique({
    where: { tokenHash },
    include: {
      creator: true,
      createdByUser: true,
    },
  });

  if (!session) {
    return {
      ok: false,
      httpStatus: 404,
      code: "SESSION_NOT_FOUND",
      error: "Connect session not found",
    };
  }

  if (session.status !== "PENDING" && session.status !== "CLAIMED") {
    return {
      ok: false,
      httpStatus: 409,
      code: "SESSION_NOT_ACTIVE",
      error: `Session is ${session.status}`,
      session,
    };
  }

  if (session.expiresAt < new Date()) {
    await prisma.creatorConnectSession.update({
      where: { id: session.id },
      data: { status: "EXPIRED" },
    });

    return {
      ok: false,
      httpStatus: 410,
      code: "SESSION_EXPIRED",
      error: "Connect session expired",
      session,
    };
  }

  return {
    ok: true,
    session,
  };
}

async function upsertPublicDevice({ agencyId, userId, deviceId, deviceName, platform, appVersion }) {
  if (!deviceId && !deviceName && !platform && !appVersion) return null;

  const id = deviceId || `device_${userId}_${Date.now()}`;

  return prisma.workerDevice.upsert({
    where: { id },
    create: {
      id,
      agencyId,
      userId,
      deviceName: deviceName || null,
      platform: platform || null,
      appVersion: appVersion || null,
      lastSeenAt: new Date(),
    },
    update: {
      deviceName: deviceName || undefined,
      platform: platform || undefined,
      appVersion: appVersion || undefined,
      lastSeenAt: new Date(),
    },
  });
}

async function completeSessionWithSnapshot({ session, snapshot, deviceId, actorUserId, action }) {
  const snapshotPayload = {
    ...snapshot,
    creatorId: session.creatorId,
    agencyId: session.agencyId,
    capturedAt: snapshot.capturedAt || new Date().toISOString(),
  };

  const encrypted = encryptSnapshot(snapshotPayload);
  const username = snapshot.username || session.creator.username || null;
  const remoteId = snapshot.remoteId || session.creator.remoteId || null;

  const result = await prisma.$transaction(async (tx) => {
    await tx.accessSnapshot.updateMany({
      where: {
        agencyId: session.agencyId,
        creatorId: session.creatorId,
        active: true,
      },
      data: {
        active: false,
        revokedAt: new Date(),
      },
    });

    const accessSnapshot = await tx.accessSnapshot.create({
      data: {
        agencyId: session.agencyId,
        creatorId: session.creatorId,
        createdByUserId: actorUserId || session.createdByUserId,
        deviceId: deviceId || session.claimedByDeviceId || null,
        encryptedPayload: encrypted.encryptedPayload,
        iv: encrypted.iv,
        tag: encrypted.tag,
        algorithm: encrypted.algorithm,
        payloadVersion: encrypted.payloadVersion,
        userAgentHash: hashUserAgent(snapshot.userAgent),
        remoteId,
        username,
        active: true,
      },
    });

    const creator = await tx.creatorAccount.update({
      where: { id: session.creatorId },
      data: {
        status: "READY",
        username,
        remoteId,
        displayName: snapshot.displayName || session.creator.displayName,
        avatarUrl: snapshot.avatarUrl || session.creator.avatarUrl,
        partition: session.creator.partition || makePartition(session.creatorId),
      },
    });

    const updatedSession = await tx.creatorConnectSession.update({
      where: { id: session.id },
      data: {
        status: "COMPLETED",
        completedAt: new Date(),
        claimedByUserId: session.claimedByUserId || actorUserId || session.createdByUserId,
        claimedByDeviceId: session.claimedByDeviceId || deviceId || null,
        claimedAt: session.claimedAt || new Date(),
      },
    });

    return {
      accessSnapshot,
      creator,
      session: updatedSession,
    };
  });

  await audit({
    agencyId: session.agencyId,
    actorUserId: actorUserId || session.createdByUserId,
    action,
    targetType: "creator",
    targetId: result.creator.id,
    metadata: {
      sessionId: session.id,
      snapshotId: result.accessSnapshot.id,
      username,
      remoteId,
      deviceId: deviceId || session.claimedByDeviceId || null,
      cookiesCount: Array.isArray(snapshot.cookies) ? snapshot.cookies.length : 0,
    },
  });

  return result;
}

router.post("/claim-public", async (req, res) => {
  try {
    const input = claimSchema.parse(req.body);
    const found = await findActiveSessionByToken(input.token);

    if (!found.ok) {
      return res.status(found.httpStatus).json({
        ok: false,
        code: found.code,
        error: found.error,
      });
    }

    const session = found.session;

    const device = await upsertPublicDevice({
      agencyId: session.agencyId,
      userId: session.createdByUserId,
      deviceId: input.deviceId,
      deviceName: input.deviceName,
      platform: input.platform,
      appVersion: input.appVersion,
    });

    const updated = await prisma.creatorConnectSession.update({
      where: { id: session.id },
      data: {
        status: "CLAIMED",
        claimedByUserId: session.claimedByUserId || session.createdByUserId,
        claimedByDeviceId: device?.id || input.deviceId || null,
        claimedAt: session.claimedAt || new Date(),
      },
    });

    await audit({
      agencyId: session.agencyId,
      actorUserId: session.createdByUserId,
      action: "creator_connect.claim_public",
      targetType: "creator_connect_session",
      targetId: session.id,
      metadata: {
        creatorId: session.creatorId,
        deviceId: device?.id || input.deviceId || null,
      },
    });

    return res.json({
      ok: true,
      session: {
        id: updated.id,
        status: updated.status,
        expiresAt: updated.expiresAt,
      },
      creator: session.creator,
      agencyId: session.agencyId,
      partition: session.creator.partition || makePartition(session.creator.id),
      loginUrl: "https://onlyfans.com",
    });
  } catch (err) {
    if (err?.issues) return validationError(res, err);
    console.error("[creator-connect/claim-public] failed:", err);
    return res.status(500).json({
      ok: false,
      code: "CONNECT_CLAIM_PUBLIC_FAILED",
      error: "Failed to claim creator connection",
    });
  }
});

